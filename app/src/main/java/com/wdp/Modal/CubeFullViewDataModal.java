package com.wdp.Modal;

public class CubeFullViewDataModal {
    String url;
    String type;

    public CubeFullViewDataModal(String url, String type) {
        this.url = url;
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
